#[allow(dead_code)]
#[derive(serde::Deserialize)]
pub struct Settings {
    pub app_port: u16,
    pub database: DatabaseSettings,
}

#[derive(serde::Deserialize)]
pub struct DatabaseSettings {
    pub username: String,
    pub password: String,
    pub port: u16,
    pub host: String,
    pub db_name: String,
}
